<script>
  import { page } from '$app/stores';

  /** @type {{ title: string }} */
  let { title } = $props();

  let copied = $state(false);

  async function copyLink() {
    try {
      await navigator.clipboard.writeText($page.url.href);
      copied = true;
      setTimeout(() => (copied = false), 2000);
    } catch {
      // fallback: select the URL
    }
  }

  let twitterUrl = $derived(
    `https://twitter.com/intent/tweet?text=${encodeURIComponent(title)}&url=${encodeURIComponent($page.url.href)}`
  );

  let linkedInUrl = $derived(
    `https://www.linkedin.com/sharing/share-offsite/?url=${encodeURIComponent($page.url.href)}`
  );
</script>

<div class="share-strip">
  <span class="text-label-sm text-on-surface-variant uppercase tracking-wider">Share</span>

  <!-- Copy Link -->
  <button onclick={copyLink} class="share-btn" aria-label="Copy link">
    {#if copied}
      <!-- Check icon -->
      <svg class="w-4 h-4" fill="none" viewBox="0 0 24 24" stroke="currentColor" stroke-width="2.5">
        <path stroke-linecap="round" stroke-linejoin="round" d="M5 13l4 4L19 7" />
      </svg>
      <span>Copied!</span>
    {:else}
      <!-- Link icon -->
      <svg class="w-4 h-4" fill="none" viewBox="0 0 24 24" stroke="currentColor" stroke-width="2">
        <path stroke-linecap="round" stroke-linejoin="round" d="M13.828 10.172a4 4 0 00-5.656 0l-4 4a4 4 0 105.656 5.656l1.102-1.101m-.758-4.899a4 4 0 005.656 0l4-4a4 4 0 00-5.656-5.656l-1.1 1.1" />
      </svg>
      <span>Copy link</span>
    {/if}
  </button>

  <!-- Twitter / X -->
  <a href={twitterUrl} target="_blank" rel="noopener noreferrer" class="share-btn" aria-label="Share on X (Twitter)">
    <svg class="w-4 h-4" fill="currentColor" viewBox="0 0 24 24">
      <path d="M18.244 2.25h3.308l-7.227 8.26 8.502 11.24H16.17l-5.214-6.817L4.99 21.75H1.68l7.73-8.835L1.254 2.25H8.08l4.713 6.231zm-1.161 17.52h1.833L7.084 4.126H5.117z" />
    </svg>
    <span>Tweet</span>
  </a>

  <!-- LinkedIn -->
  <a href={linkedInUrl} target="_blank" rel="noopener noreferrer" class="share-btn" aria-label="Share on LinkedIn">
    <svg class="w-4 h-4" fill="currentColor" viewBox="0 0 24 24">
      <path d="M20.447 20.452h-3.554v-5.569c0-1.328-.027-3.037-1.852-3.037-1.853 0-2.136 1.445-2.136 2.939v5.667H9.351V9h3.414v1.561h.046c.477-.9 1.637-1.85 3.37-1.85 3.601 0 4.267 2.37 4.267 5.455v6.286zM5.337 7.433c-1.144 0-2.063-.926-2.063-2.065 0-1.138.92-2.063 2.063-2.063 1.14 0 2.064.925 2.064 2.063 0 1.139-.925 2.065-2.064 2.065zm1.782 13.019H3.555V9h3.564v11.452zM22.225 0H1.771C.792 0 0 .774 0 1.729v20.542C0 23.227.792 24 1.771 24h20.451C23.2 24 23.2 23.227 23.2 22.271V1.729C23.2.774 23.2 0 22.222 0h.003z"/>
    </svg>
    <span>LinkedIn</span>
  </a>
</div>
